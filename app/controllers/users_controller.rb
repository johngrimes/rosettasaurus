class UsersController < ApplicationController
  layout 'standard'
  

  # render new.rhtml
  def new
    @user = User.new
  end
 
  def create
    logout_keeping_session!
    @user = User.new(params[:user])
    success = @user && @user.save
    if success && @user.errors.empty?
      redirect_to help_url(:action => 'activation')
    else
      flash[:error]  = "We couldn't set up that account, sorry.  Please try again, or contact feedback@rosettasaurus.com."
      render :action => 'new'
    end
  end

  def activate
    logout_keeping_session!
    user = User.find_by_activation_code(params[:activation_code]) unless params[:activation_code].blank?
    case
    when (!params[:activation_code].blank?) && user && !user.active?
      user.activate!
      flash[:notice] = "Your account has now been activated. Please log in to continue."
      redirect_to '/login'
    when params[:activation_code].blank?
      flash[:error] = "The activation code was missing.  Please follow the URL from your email."
      redirect_back_or_default('/')
    else 
      flash[:error]  = "We couldn't find a user with that activation code - check your email? Or maybe you've already activated - try logging in."
      redirect_back_or_default('/')
    end
  end
end
